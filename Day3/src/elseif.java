
public class elseif {

	public static void main(String[] args) {
		String lang="Java";
		if(lang=="Python")
		{
			System.out.println("Language is Python");
		} else if(lang=="C++")
		{
			System.out.println("Language is C++");
		} else if(lang=="Java")
		{
			System.out.println("Language is Java");
		} else
		{
			System.out.println("lang");
		}

	}

}
